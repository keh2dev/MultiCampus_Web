package 복습;

public class 기본형심화2 {

	public static void main(String[] args) {
		// 정수형 4가지
		// -128~127, 1바이트 byte
		// -3만~3만, 2바이트 short
		// -21억~21억, 4바이트 int
		// 21억 이상, 8바이트 long
		
		byte age = 127; // 128부터 불
		short days = 1000;
		int money = 100000; // ***
		long bank = 2300000000L; // long의 경우 뒤에 대문자 L 또는 소문자 l 을 넣어야한다.
		
		// 실수형 변수 2가지
		// 소수점 4자리까지 4바이트 float
		// 소수점 4자리 이상 8바이트 double
		double weight = 43.3333; // ***
		float height = 170.4F; // float의 경우 뒤에 대문자 F 또는 소문자 f 를 넣어야한다.
		
		// 문자1 char
		// 논리 boolean
		
	}

}
